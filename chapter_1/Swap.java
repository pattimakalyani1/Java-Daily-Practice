class Swap
{
	public static void main(String [] args)
	{
	int n1=12;
        int n2=24;
        int n3;
	System.out.println("before swapping");
	System.out.println("n1: "+n1);
	System.out.println("n2: "+n2);
	n3=n1;
	n1=n2;
	n2=n3;
	System.out.println("after swapping");
	System.out.println("n1: "+n1);
	System.out.println("n2: "+n2);
	}
}

	
	